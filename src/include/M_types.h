#ifndef M_TYPES_H
#define M_TYPES_H
#include <time.h>

#define FALSE  0
#define TRUE   1

#ifndef BOOL
typedef unsigned char   BOOL;
#endif

#ifndef SINT8
typedef char		SINT8;
#endif

#ifndef SINT16
typedef short		SINT16;
#endif

#ifndef SINT32
typedef int		SINT32;
#endif

#ifndef SINT64
typedef long long	SINT64;
#endif

#ifndef UINT8
typedef unsigned char	UINT8;
#endif

#ifndef UINT16
typedef unsigned short	UINT16;
#endif

#ifndef UINT32
typedef unsigned int	UINT32;
#endif

#ifndef UINT64
typedef unsigned long long UINT64;
#endif

#ifndef FLOAT
typedef float FLOAT;
#endif

#ifndef DOUBLE
typedef double DOUBLE;
#endif

#ifndef LONG_DOUBLE
typedef long double LONG_DOUBLE;
#endif

#ifndef BYTE
typedef unsigned char BYTE;
#endif

#ifndef INT32
typedef unsigned int INT32;
#endif

#endif /*M_TYPES_H*/
